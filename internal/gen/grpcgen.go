package gen

import (
	"fmt"

	"google.golang.org/protobuf/compiler/protogen"
)

// Import paths used by generated gRPC code. Using protogen.GoImportPath makes
// protogen manage the import block automatically.
var (
	contextPkg = protogen.GoImportPath("context")
	grpcPkg    = protogen.GoImportPath("google.golang.org/grpc")
	codesPkg   = protogen.GoImportPath("google.golang.org/grpc/codes")
	statusPkg  = protogen.GoImportPath("google.golang.org/grpc/status")
)

// GRPC is a compact stand-in for protoc-gen-go-grpc. It emits client and server
// stubs for all four RPC kinds (unary, server/client/bidi streaming), modeled on
// protoc-gen-go-grpc's modern generics-based output.
type GRPC struct{}

func (GRPC) Name() string { return "grpc" }

func (GRPC) Generate(gen *protogen.Plugin) error {
	for _, f := range gen.Files {
		if !f.Generate || len(f.Services) == 0 {
			continue
		}
		genGRPCFile(gen, f)
	}
	return nil
}

func genGRPCFile(gen *protogen.Plugin, f *protogen.File) {
	g := gen.NewGeneratedFile(f.GeneratedFilenamePrefix+"_grpc.pb.go", f.GoImportPath)
	g.P("// Code generated by protogenall. DO NOT EDIT.")
	g.P("// source: ", f.Desc.Path())
	g.P()
	g.P("package ", f.GoPackageName)
	g.P()

	for _, svc := range f.Services {
		genService(g, svc)
	}
}

func genService(g *protogen.GeneratedFile, svc *protogen.Service) {
	genClient(g, svc)
	genServer(g, svc)
	genServiceDesc(g, svc)
}

// ---- helpers ----

func fullMethodName(svc *protogen.Service, m *protogen.Method) string {
	return fmt.Sprintf("/%s/%s", svc.Desc.FullName(), m.Desc.Name())
}

func handlerName(svc *protogen.Service, m *protogen.Method) string {
	return fmt.Sprintf("_%s_%s_Handler", svc.GoName, m.GoName)
}

// streamIndex maps each streaming method to its position in ServiceDesc.Streams.
func streamIndex(svc *protogen.Service) map[*protogen.Method]int {
	idx := map[*protogen.Method]int{}
	n := 0
	for _, m := range svc.Methods {
		if isStreaming(m) {
			idx[m] = n
			n++
		}
	}
	return idx
}

func isStreaming(m *protogen.Method) bool {
	return m.Desc.IsStreamingClient() || m.Desc.IsStreamingServer()
}

// generic renders grpc.<name>[params...].
func (h *idents) generic(name string, params ...string) string {
	out := h.g.QualifiedGoIdent(grpcPkg.Ident(name)) + "["
	for i, p := range params {
		if i > 0 {
			out += ", "
		}
		out += p
	}
	return out + "]"
}

// idents caches per-file qualified identifiers.
type idents struct {
	g          *protogen.GeneratedFile
	ctx        string
	callOpt    string
	clientConn string
}

func newIdents(g *protogen.GeneratedFile) *idents {
	return &idents{
		g:          g,
		ctx:        g.QualifiedGoIdent(contextPkg.Ident("Context")),
		callOpt:    g.QualifiedGoIdent(grpcPkg.Ident("CallOption")),
		clientConn: g.QualifiedGoIdent(grpcPkg.Ident("ClientConnInterface")),
	}
}

func (h *idents) in(m *protogen.Method) string  { return h.g.QualifiedGoIdent(m.Input.GoIdent) }
func (h *idents) out(m *protogen.Method) string { return h.g.QualifiedGoIdent(m.Output.GoIdent) }
