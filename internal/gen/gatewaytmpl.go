// Import paths used by generated gateway code.
var (
	httpPkg      = protogen.GoImportPath("net/http")
	errorsPkg    = protogen.GoImportPath("errors")
	ioPkg        = protogen.GoImportPath("io")
	protoRefPkg  = protogen.GoImportPath("google.golang.org/protobuf/proto")
	runtimePkg   = protogen.GoImportPath("github.com/grpc-ecosystem/grpc-gateway/v2/runtime")
	utilitiesPkg = protogen.GoImportPath("github.com/grpc-ecosystem/grpc-gateway/v2/utilities")
	grpclogPkg   = protogen.GoImportPath("google.golang.org/grpc/grpclog")
	metadataPkg  = protogen.GoImportPath("google.golang.org/grpc/metadata")
)

func genGatewayFile(gen *protogen.Plugin, f *protogen.File, bindings map[*protogen.Service][]binding) {
	g := gen.NewGeneratedFile(f.GeneratedFilenamePrefix+".pb.gw.go", f.GoImportPath)
	g.P("// Code generated by protogenall. DO NOT EDIT.")
	g.P("// source: ", f.Desc.Path())
	g.P("//")
	g.P("// Reverse proxy: translates RESTful JSON into gRPC.")
	g.P()
	g.P("package ", f.GoPackageName)
	g.P()

	for _, svc := range f.Services {
		bs := bindings[svc]
		if len(bs) == 0 {
			continue
		}
		for i := range bs {
			if bs[i].serverStream {
				genStreamRequestDecoder(g, svc, &bs[i]) // request_ (client) only
				continue
			}
			genRequestDecoder(g, svc, &bs[i], false) // request_ (client)
			genRequestDecoder(g, svc, &bs[i], true)  // local_request_ (server)
		}
		genRegisterHandlerServer(g, svc, bs)
		genRegisterFromEndpoint(g, svc)
		genRegisterHandler(g, svc)
		genRegisterHandlerClient(g, svc, bs)
		genPatternsAndForwards(g, svc, bs)
	}
}

// ctxType etc. resolve common qualified idents lazily per file.
func fullMethod(svc *protogen.Service, m *protogen.Method) string {
	return fmt.Sprintf("/%s/%s", svc.Desc.FullName(), m.Desc.Name())
}

func handlerBase(svc *protogen.Service, m *protogen.Method) string {
	return fmt.Sprintf("%s_%s_0", svc.GoName, m.GoName)
}

// genRequestDecoder emits request_<h> (client) or local_request_<h> (server).
func genRequestDecoder(g *protogen.GeneratedFile, svc *protogen.Service, b *binding, local bool) {
	m := b.method
	base := handlerBase(svc, m)
	name := "request_" + base
	callee := "client " + svc.GoName + "Client"
	if local {
		name = "local_request_" + base
		callee = "server " + svc.GoName + "Server"
	}

	ctx := g.QualifiedGoIdent(contextPkg.Ident("Context"))
	marshaler := g.QualifiedGoIdent(runtimePkg.Ident("Marshaler"))
	serverMD := g.QualifiedGoIdent(runtimePkg.Ident("ServerMetadata"))
	httpReq := "*" + g.QualifiedGoIdent(httpPkg.Ident("Request"))
	protoMsg := g.QualifiedGoIdent(protoRefPkg.Ident("Message"))
	in := g.QualifiedGoIdent(m.Input.GoIdent)

	g.P("func ", name, "(ctx ", ctx, ", marshaler ", marshaler, ", ", callee, ", req ", httpReq, ", pathParams map[string]string) (", protoMsg, ", ", serverMD, ", error) {")
	g.P("var protoReq ", in)
	g.P("var metadata ", serverMD)
	needErr := len(b.pathParams) > 0 || hasQuery(b)
	if needErr {
		g.P("var err error")
		g.P("_ = err")
	}

	genBodyDecode(g, b)
	genPathParams(g, b)
	genQueryParams(g, b)

	if local {
		g.P("msg, err := server.", m.GoName, "(ctx, &protoReq)")
	} else {
		grpcHeader := g.QualifiedGoIdent(grpcPkg.Ident("Header"))
		grpcTrailer := g.QualifiedGoIdent(grpcPkg.Ident("Trailer"))
		g.P("msg, err := client.", m.GoName, "(ctx, &protoReq, ", grpcHeader, "(&metadata.HeaderMD), ", grpcTrailer, "(&metadata.TrailerMD))")
	}
	g.P("return msg, metadata, err")
	g.P("}")
	g.P()
}

// genStreamRequestDecoder emits request_<h> for a server-streaming method: it
// opens the gRPC stream and returns the client stream for the handler to pump.
func genStreamRequestDecoder(g *protogen.GeneratedFile, svc *protogen.Service, b *binding) {
	m := b.method
	base := handlerBase(svc, m)

	ctx := g.QualifiedGoIdent(contextPkg.Ident("Context"))
	marshaler := g.QualifiedGoIdent(runtimePkg.Ident("Marshaler"))
	serverMD := g.QualifiedGoIdent(runtimePkg.Ident("ServerMetadata"))
	httpReq := "*" + g.QualifiedGoIdent(httpPkg.Ident("Request"))
	in := g.QualifiedGoIdent(m.Input.GoIdent)
	streamType := g.QualifiedGoIdent(grpcPkg.Ident("ServerStreamingClient")) + "[" + g.QualifiedGoIdent(m.Output.GoIdent) + "]"

	g.P("func request_", base, "(ctx ", ctx, ", marshaler ", marshaler, ", client ", svc.GoName, "Client, req ", httpReq, ", pathParams map[string]string) (", streamType, ", ", serverMD, ", error) {")
	g.P("var protoReq ", in)
	g.P("var metadata ", serverMD)
	if len(b.pathParams) > 0 || hasQuery(b) {
		g.P("var err error")
		g.P("_ = err")
	}

	genBodyDecode(g, b)
	genPathParams(g, b)
	genQueryParams(g, b)

	g.P("stream, err := client.", m.GoName, "(ctx, &protoReq)")
	g.P("if err != nil { return nil, metadata, err }")
	g.P("header, err := stream.Header()")
	g.P("if err != nil { return nil, metadata, err }")
	g.P("metadata.HeaderMD = header")
	g.P("return stream, metadata, nil")
	g.P("}")
	g.P()
}

func genBodyDecode(g *protogen.GeneratedFile, b *binding) {
	codes := runtimeCodes(g)
	switch b.body {
	case "*":
		g.P("if err := marshaler.NewDecoder(req.Body).Decode(&protoReq); err != nil && !", g.QualifiedGoIdent(errorsPkg.Ident("Is")), "(err, ", g.QualifiedGoIdent(ioPkg.Ident("EOF")), ") {")
		g.P("return nil, metadata, ", codes.statusErrf, "(", codes.invalidArg, `, "%v", err)`)
		g.P("}")
	case "":
		// No body: drain it so keep-alive works.
		g.P("if req.Body != nil {")
		g.P("_, _ = ", g.QualifiedGoIdent(ioPkg.Ident("Copy")), "(", g.QualifiedGoIdent(ioPkg.Ident("Discard")), ", req.Body)")
		g.P("}")
	default:
		// body bound to a specific field: decode into that Go field.
		g.P("if err := marshaler.NewDecoder(req.Body).Decode(&protoReq); err != nil && !", g.QualifiedGoIdent(errorsPkg.Ident("Is")), "(err, ", g.QualifiedGoIdent(ioPkg.Ident("EOF")), ") {")
		g.P("return nil, metadata, ", codes.statusErrf, "(", codes.invalidArg, `, "%v", err)`)
		g.P("}")
	}
}

func genPathParams(g *protogen.GeneratedFile, b *binding) {
	codes := runtimeCodes(g)
	for _, fld := range b.pathParams {
		conv, ok := runtimeConv(fld.Desc.Kind())
		pname := string(fld.Desc.Name())
		g.P("val, ok := pathParams[", strconv.Quote(pname), "]")
		g.P("if !ok {")
		g.P("return nil, metadata, ", codes.statusErrf, "(", codes.invalidArg, `, "missing parameter %s", `, strconv.Quote(pname), ")")
		g.P("}")
		if !ok {
			g.P("_ = val // unsupported path-param kind ", fld.Desc.Kind().String(), "; left unset")
			continue
		}
		g.P("protoReq.", fld.GoName, ", err = ", g.QualifiedGoIdent(runtimePkg.Ident(conv)), "(val)")
		g.P("if err != nil {")
		g.P("return nil, metadata, ", codes.statusErrf, "(", codes.invalidArg, `, "type mismatch, parameter: %s, error: %v", `, strconv.Quote(pname), ", err)")
		g.P("}")
	}
}

func genQueryParams(g *protogen.GeneratedFile, b *binding) {
	if !hasQuery(b) {
		return
	}
	codes := runtimeCodes(g)
	// Build a filter excluding path-param fields.
	var paths []string
	for _, fld := range b.pathParams {
		paths = append(paths, strconv.Quote(string(fld.Desc.Name())))
	}
	filter := fmt.Sprintf("%s([][]string{%s})", g.QualifiedGoIdent(utilitiesPkg.Ident("NewDoubleArray")), wrapPaths(paths))
	g.P("if err := req.ParseForm(); err != nil {")
	g.P("return nil, metadata, ", codes.statusErrf, "(", codes.invalidArg, `, "%v", err)`)
	g.P("}")
	g.P("if err := ", g.QualifiedGoIdent(runtimePkg.Ident("PopulateQueryParameters")), "(&protoReq, req.Form, ", filter, "); err != nil {")
	g.P("return nil, metadata, ", codes.statusErrf, "(", codes.invalidArg, `, "%v", err)`)
	g.P("}")
}

// hasQuery reports whether any request field is not bound by path or a "*" body.
func hasQuery(b *binding) bool {
	if b.body == "*" {
		return false
	}
	inPath := map[string]bool{}
	for _, f := range b.pathParams {
		inPath[string(f.Desc.Name())] = true
	}
	for _, f := range b.method.Input.Fields {
		if !inPath[string(f.Desc.Name())] {
			return true
		}
	}
	return false
}

func wrapPaths(quoted []string) string {
	var parts []string
	for _, q := range quoted {
		parts = append(parts, "{"+q+"}")
	}
	return strings.Join(parts, ", ")
}

type runtimeIdents struct {
	statusErrf string
	invalidArg string
}

func runtimeCodes(g *protogen.GeneratedFile) runtimeIdents {
	return runtimeIdents{
		statusErrf: g.QualifiedGoIdent(statusPkg.Ident("Errorf")),
		invalidArg: g.QualifiedGoIdent(codesPkg.Ident("InvalidArgument")),
	}
}

func runtimeConv(kind protoreflect.Kind) (string, bool) {
	switch kind {
	case protoreflect.StringKind:
		return "String", true
	case protoreflect.BoolKind:
		return "Bool", true
	case protoreflect.Int32Kind, protoreflect.Sint32Kind, protoreflect.Sfixed32Kind:
		return "Int32", true
	case protoreflect.Int64Kind, protoreflect.Sint64Kind, protoreflect.Sfixed64Kind:
		return "Int64", true
	case protoreflect.Uint32Kind, protoreflect.Fixed32Kind:
		return "Uint32", true
	case protoreflect.Uint64Kind, protoreflect.Fixed64Kind:
		return "Uint64", true
	case protoreflect.FloatKind:
		return "Float32", true
	case protoreflect.DoubleKind:
		return "Float64", true
	}
	return "", false
}
